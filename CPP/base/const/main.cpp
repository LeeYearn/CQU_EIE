#include <iostream>

int main()
{
    int a = 10;
    int b = 20;
    int c = 30;

    //常量指针：指针指向的值不能改变，指针的值(地址)可以改变
    const int* p = &a;
    /*
    *p = 20; 错误-指针指向的值不能改变
     p = &b; 正确-指针的值(地址)可以改变
    */

    //指针常量：指针的值(地址)不能改变，指针指向的值可以改变
    int* const q = &a;
    /*
    *q = 20; 正确-指针指向的值可以改变
     q = &b; 错误-指针的值(地址)不能改变
    */

    //常量指针常量：指针的值(地址)不能改变，指针指向的值不能改变
    const int* const r = &a;
    /*
    *r = 20; 错误-指针指向的值不能改变
     r = &b; 错误-指针的值(地址)不能改变
    */
    return 0;
}